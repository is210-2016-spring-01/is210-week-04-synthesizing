"""Fahrenheit to Celsius Calculator"""

def fahrenheit_to_celsuis(degrees):
    """Converts fahrenheit into Celsius.

    Args:
        degrees

    Returns:
        

    Examples:
        >>> fahrenheit_to_celsius(212)
        Decimal('100')
    """

    return degrees * 9/5 + 32
